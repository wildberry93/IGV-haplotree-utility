package VCFregion;

public class HaplotypeSequence {

	String HaploRef = "";
	String HaploAlt = "";
	String SampleName = "";
	
	public HaplotypeSequence(String HaploRef, String HaploAlt, String SampleName){
		this.HaploRef = HaploRef;
 		this.HaploAlt = HaploAlt;
 		this.SampleName = SampleName;
	}
	
}